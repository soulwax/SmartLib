export type ColorSchemeId = (typeof COLOR_SCHEMES)[number]["id"]

export const DEFAULT_COLOR_SCHEME_ID: ColorSchemeId = "github-dark"

const COLOR_SCHEME_BY_ID = new Map<string, ColorSchemeDefinition>(
  COLOR_SCHEMES.map((scheme) => [scheme.id, scheme] as const)
)

export function isColorSchemeId(value: string): value is ColorSchemeId {
  return COLOR_SCHEME_BY_ID.has(value)
}

export function getColorSchemeById(
  value: string
): ColorSchemeDefinition | undefined {
  return COLOR_SCHEME_BY_ID.get(value)
}

export function normalizeColorSchemeId(value: string | null | undefined): ColorSchemeId {
  if (typeof value !== "string") {
    return DEFAULT_COLOR_SCHEME_ID
  }

  const trimmed = value.trim()
  if (!trimmed || !isColorSchemeId(trimmed)) {
    return DEFAULT_COLOR_SCHEME_ID
  }

  return trimmed
}
